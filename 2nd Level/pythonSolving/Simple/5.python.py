#Calculate all Arithmetic operation On Three Variables
a = int(input("Enter the first number: "))
b = int(input("Enter the second number: "))
c = int(input("Enter the third number: "))
print("The Product is = "+ str(a*b*c))
print("The Addition is = "+ str(a+b+c))
print("The Subtraction is = "+ str(a-b-c))
print("The Division is = "+ str(a/b/c))
print("The Modulus is = "+ str(a%b%c))
